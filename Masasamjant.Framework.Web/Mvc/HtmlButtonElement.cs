namespace Masasamjant.Web.Mvc
{
    /// <summary>
    /// Defines what element is used as button element.
    /// </summary>
    public enum HtmlButtonElement : int
    {
        /// <summary>
        /// Button element is <c>button</c>.
        /// </summary>
        Button = 0,

        /// <summary>
        /// Button element is <c>a</c>.
        /// </summary>
        Hyperlink = 1
    }
}
